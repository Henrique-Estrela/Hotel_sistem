package usuarios;
class Funcionario {
    private int codigo;
    private String nome;
    private String cargo;
    
    //Criação dos Getters e Setters dos atributos de funcionario
    public int getCodigo() {
        return codigo;
    }
    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getCargo() {
        return cargo;
    }
    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    
}